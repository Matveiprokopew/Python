#------Цикл------
while(True):
#------Запрос чисел------
   a=int(input("Введите число: "))
   b=int(input("Введите число: "))
   v=str(input("Введите знак(+ - * /): "))
#------Сложение------
   if v=="+" :
        sum=a+b
        print(sum)
#-------Вычитание------
   if v=="-" :
       sum=a-b
       print(sum)
#------Умножение------
   if v=="*" :
        sum=a*b
        print(sum)
#------Деление------
   if v=="/" :
        sum=a/b
        print(sum)
